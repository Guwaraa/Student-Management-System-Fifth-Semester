     */
    public function store(Request $request)
    {
        $id=$request->get('id');
        $data=maplocation::find($id);
        $data->college_name=$request->get('collegename');
        $data->location=$request->get('location');
        $data->map_source=$request->get('source');
        $data->save();
        $location=maplocation::get();
        return view('admin.examdetail.location.index',compact('location'));


